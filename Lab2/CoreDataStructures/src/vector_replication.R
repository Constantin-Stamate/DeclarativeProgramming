# Create a vector 'k' by repeating the sequence 3 times
k <- rep(c(8, 2, 6), times = 3)
print(k)

# Create a vector 'w' by repeating values individually
w <- c(rep(4, 7), rep(9, 5), rep(2, 3))
print(w)

# Create a vector 'q' by repeating each value a specified number of times
q <- rep(c(4, 9, 2), times = c(7, 5, 3))
print(q)